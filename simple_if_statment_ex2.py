# demonstarting whether the given number is postive or negative or zero with simple if statment
n=int(input("Enter any number: "))
print("*"*50)
if(n>0):
    print("{} is positive number".format(n))
if(n<0):
    print("{} is negative number.".format(n))
if(n==0):
    print("{} is zero.".format(n))
print("*"*50)
